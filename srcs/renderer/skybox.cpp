// ─────────────────────────────────────────────────────────────────────────────
// skybox.cpp — スカイボックスの初期化と描画
//
// 【スカイボックスとは？】
//   プレイヤーを取り囲む巨大な立方体で、内側に空の絵を描く技法。
//   カメラが移動しても「遠くにある空」が追いかけてくるように見せるため、
//   View 行列の移動成分（X/Y/Z への平行移動）を取り除いた 3×3 部分行列だけを使う。
//   こうすることでカメラを回転しても空がついてくるが、歩いても空は動かない。
//
// 【実装の仕組み】
//   1. 単位立方体（辺の長さ1）の頂点データを GPU に転送する
//   2. 描画時は深度比較を GL_LEQUAL（≤）にする
//      → スカイボックスを「深度バッファの最大値（1.0）に張り付いた状態」で
//        描画することで、常に一番遠くに見える（他の描画物に隠れる）
//   3. 描画後は深度比較を GL_LESS（<）に戻す
//
// 【CCW（Counter Clock Wise）巻き順とは？】
//   三角形の頂点を反時計回りに並べる規則。
//   OpenGL は GL_CULL_FACE を有効にすると、時計回りの面（裏面）を描画しない。
//   スカイボックスは内側から見るので、各面の頂点を「外から見て反時計回り」
//   = 「内から見て時計回り」に並べることで裏面カリングを無効にする必要がある。
//   ここでは外側から見て CCW の並びにしており、カリングで内面は描かれない…
//   が、スカイボックスはプレイヤーの内側にあるため問題なく描画される。
// ─────────────────────────────────────────────────────────────────────────────
#include "skybox.hpp"
#include "shader.hpp"
#include <glad/gl.h>

// ─────────────────────────────────────────────────────────────────────────────
// 単位立方体の頂点データ — 36頂点（6面 × 2三角形 × 3頂点）
//
// 各面の頂点は、外側から見て反時計回り（CCW）に並んでいる。
// シェーダーではこの XYZ 座標を direction ベクトルとして使い、
// 空の色（天頂→地平線→地面）を Y 値でグラデーション計算する。
// ─────────────────────────────────────────────────────────────────────────────
static const float SKY_VERTS[] = {
    // 後面 (-Z)
    -1.0f,  1.0f, -1.0f,
    -1.0f, -1.0f, -1.0f,
     1.0f, -1.0f, -1.0f,
     1.0f, -1.0f, -1.0f,
     1.0f,  1.0f, -1.0f,
    -1.0f,  1.0f, -1.0f,

    // 左面 (-X)
    -1.0f, -1.0f,  1.0f,
    -1.0f, -1.0f, -1.0f,
    -1.0f,  1.0f, -1.0f,
    -1.0f,  1.0f, -1.0f,
    -1.0f,  1.0f,  1.0f,
    -1.0f, -1.0f,  1.0f,

    // 右面 (+X)
     1.0f, -1.0f, -1.0f,
     1.0f, -1.0f,  1.0f,
     1.0f,  1.0f,  1.0f,
     1.0f,  1.0f,  1.0f,
     1.0f,  1.0f, -1.0f,
     1.0f, -1.0f, -1.0f,

    // 前面 (+Z)
    -1.0f, -1.0f,  1.0f,
    -1.0f,  1.0f,  1.0f,
     1.0f,  1.0f,  1.0f,
     1.0f,  1.0f,  1.0f,
     1.0f, -1.0f,  1.0f,
    -1.0f, -1.0f,  1.0f,

    // 上面 (+Y)
    -1.0f,  1.0f, -1.0f,
     1.0f,  1.0f, -1.0f,
     1.0f,  1.0f,  1.0f,
     1.0f,  1.0f,  1.0f,
    -1.0f,  1.0f,  1.0f,
    -1.0f,  1.0f, -1.0f,

    // 下面 (-Y)
    -1.0f, -1.0f, -1.0f,
    -1.0f, -1.0f,  1.0f,
     1.0f, -1.0f, -1.0f,
     1.0f, -1.0f, -1.0f,
    -1.0f, -1.0f,  1.0f,
     1.0f, -1.0f,  1.0f,
};

// ─────────────────────────────────────────────────────────────────────────────
// init() — GPU バッファを確保して頂点データを転送する
//
// VAO: 頂点属性レイアウト（位置 xyz のみ、3 float ずつ）を記録する
// VBO: 頂点座標データを格納する
// ─────────────────────────────────────────────────────────────────────────────
bool Skybox::init() {
    glGenVertexArrays(1, &vao_);
    glGenBuffers(1, &vbo_);

    glBindVertexArray(vao_);
    glBindBuffer(GL_ARRAY_BUFFER, vbo_);
    glBufferData(GL_ARRAY_BUFFER, sizeof(SKY_VERTS), SKY_VERTS, GL_STATIC_DRAW);

    // location 0: 位置 xyz のみ（UV や法線は不要）
    glEnableVertexAttribArray(0);
    glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 3 * sizeof(float), (void*)0);

    glBindVertexArray(0);
    glBindBuffer(GL_ARRAY_BUFFER, 0);
    return true;
}

// ─────────────────────────────────────────────────────────────────────────────
// draw() — スカイボックスを描画する
//
// view3x3: View 行列の回転成分だけ（3×3、移動なし）
// proj4x4: 通常の透視投影行列
//
// 深度テスト GL_LEQUAL にすることで、チャンクを全部描画した後でも
// スカイボックスが「チャンクの奥に見える」状態を保てる。
// ─────────────────────────────────────────────────────────────────────────────
void Skybox::draw(const float* view3x3, const float* proj4x4, Shader& shader) {
    glDepthFunc(GL_LEQUAL);  // 深度値が ≤ のピクセルも描画（スカイボックス用）

    shader.use();
    shader.setMat4("uView", view3x3);  // 回転のみの View 行列（3×3 部分）
    shader.setMat4("uProj", proj4x4);

    glBindVertexArray(vao_);
    glDrawArrays(GL_TRIANGLES, 0, 36);  // 36頂点 = 6面 × 2三角 × 3頂点
    glBindVertexArray(0);

    glDepthFunc(GL_LESS);  // 通常の深度比較に戻す
}

// ─────────────────────────────────────────────────────────────────────────────
// destroy() / デストラクタ
// ─────────────────────────────────────────────────────────────────────────────
void Skybox::destroy() {
    if (vao_) { glDeleteVertexArrays(1, &vao_); vao_ = 0; }
    if (vbo_) { glDeleteBuffers(1, &vbo_);       vbo_ = 0; }
}

Skybox::~Skybox() {
    destroy();
}
